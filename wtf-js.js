function WTFJS(text) {
  try {
    return TextToWTF(text)
  } catch (error) {
    return TextToWTF('You should pass a String')
  }
}

function TextToWTF(text) {
  return text.split('')
    .map(ch => dictionary[ch.toLowerCase()] && dictionary[ch.toLowerCase()])
    .filter(el => el != '')
    .join('+')
}

